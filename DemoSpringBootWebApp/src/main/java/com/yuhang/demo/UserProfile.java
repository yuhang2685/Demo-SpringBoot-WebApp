package com.yuhang.demo;

public class UserProfile {
	private int uid;
	private String uname;
	private String uoccupation;
	private String uarea;
	private String uurl;
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getUoccupation() {
		return uoccupation;
	}
	public void setUoccupation(String uoccupation) {
		this.uoccupation = uoccupation;
	}
	public String getUarea() {
		return uarea;
	}
	public void setUarea(String uarea) {
		this.uarea = uarea;
	}
	public String getUurl() {
		return uurl;
	}
	public void setUurl(String uurl) {
		this.uurl = uurl;
	}
	
	
	
}
